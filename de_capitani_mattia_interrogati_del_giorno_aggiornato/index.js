var intervieweeNumberDay, timeNumber
var studentNumber = students.normal.length + students.dsa.length

var step1Button = document.querySelector("#step1ok")

step1Button.onclick = function () {
    intervieweeNumberDay = document.querySelector("#interviewee-number-day").value
    timeNumber = Math.ceil(studentNumber / intervieweeNumberDay)
    let step2Div = document.querySelector("#step2"), optionSelect
    // creo il numero di opzioni
    for (let i = 0; i < timeNumber; i++)optionSelect += `<option value="${i}">Volta ${i + 1}</option >`
    // Inserisco il select per ogni dsa
    for(let i = 0; i < students.dsa.length; i++){
        // let label = document.createElement('label')
        // label.innerText = students.dsa[i]
        // console.log(label)
        step2Div.innerHTML += `<label>${students.dsa[i]}</label>` 
        step2Div.innerHTML += `
            <select class="dsaSelect">
                ${optionSelect}
            </select>
        `
    }
    document.querySelector("#step2").style.visibility = "visible"
    document.querySelector("#step2ok").style.visibility = "visible"
    document.querySelector("#step1").outerHTML = null
    //creo oggetto calendario
    for (let i = 0; i < timeNumber; i++) {
        calendarObject.push([])
    }
}
var calendarObject = []

function clickStep2Button() {
    
    // Recupero le scelte dei dsa e le inserisco nelle loro posizioni
    let dsaSelect = document.querySelectorAll(".dsaSelect")
    for (let i = 0; i < dsaSelect.length; i++) calendarObject[dsaSelect[i].value].push(students.dsa[i])
    //randomizzo array studenti normali
    students.normal.sort(function () {
        return Math.random() - 0.5
    })
    // Inserisco nell'oggetto i nomi delle persone non dsa
    for(let i = 0; i < calendarObject.length; i++){
        while ((calendarObject[i].length < intervieweeNumberDay) && (students.normal.length > 0)){
            calendarObject[i].push(students.normal[0])
            students.normal.shift()
        }
    }
    
    console.log(calendarObject)
    document.querySelector("#step2").outerHTML = null
    document.querySelector("#step2ok").outerHTML = null
    print()
}

function print() {
    let div = document.querySelector("#step3")
    document.querySelector("#who").style.visibility = "visible"
    for(let i = 0; i < calendarObject.length; i++){
        let littleDiv = `<div class=\"flexboxizzami\"><span id="time">volta ${i+1}</span>`
        for (let j = 0; j < calendarObject[i].length; j++) {
            littleDiv += `
                <p>${calendarObject[i][j]}</p>
            `

        }

        div.innerHTML += littleDiv + "</div>"
    }
}



